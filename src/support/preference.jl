#=>
    This package has a few configurations available.

    Universal Mean Time (UMT)
       Timekeeping that follows civil convention
       and wobbles with astronometric wave energy.
       Universal Times are times given an observer
       [almost] anchored on the Greenwich Meridan.
       Universal Mean Time is without leap seconds.

     Local Mean Time (LMT)
       Timekeeping that follows civil convention
       and wobbles with astronometric wave energy.
       Local Times are times given for an observer
       at the present location.  Local Mean Times
       are given with respect to local time zone.
       This is the time given by a very accurate
       wall clock. Leap seconds are ingnored.

<=#

